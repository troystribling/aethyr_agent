class FileTermination < ActiveRecord::Base

  ######################################################################################################
  #### inheritance relations
  has_ancestor :named => :aln_termination   

  ######################################################################################################
  #### validation
  validates_presence_of  :name,    :if => :name_required?

  ######################################################################################################
  #### restrict attribute access

######################################################################################################
protected

  ####################################################################################################
  def name_required?
    self.name.blank?
  end

end

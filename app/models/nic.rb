class Nic < ActiveRecord::Base

  ######################################################################################################
  #### mixins
  include Aethyr::Mixins::Synchronizer::Model
  extend Aethyr::Aln::ConnectedModelHelper  

  ######################################################################################################
  #### inheritance relations
  has_ancestor :named => :aln_resource   

  ######################################################################################################
  #### validation
  validates_presence_of  :mac_address
  validates_presence_of  :ip_address
  validates_presence_of  :physical_id

  ####################################################################################################
  def add_associations

    #### supporter relation
    supporter = System.find_by_model(:first)
    supporter << self
    
  end

  ######################################################################################################
  #### class methods
  class << self
  end  

######################################################################################################
protected


end

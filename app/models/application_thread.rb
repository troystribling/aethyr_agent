class ApplicationThread < ActiveRecord::Base

  ######################################################################################################
  #### inheritance relations
  has_ancestor :named => :aln_resource   

  ######################################################################################################
  #### mixins
  include Aethyr::Mixins::Synchronizer::Model
  extend Aethyr::Aln::ConnectedModelHelper  

  ######################################################################################################
  #### virtual attributes
  attr_accessor :tt, :sess, :pid

  ######################################################################################################
  #### validation

  ####################################################################################################
  def add_associations(supporter)

    supporter << self

  end

  ######################################################################################################
  #### class methods
  class << self
    
    ####################################################################################################
    def sync_key(params)
      params[:name] = params[:lwp]
      params[:name]
    end
    
    ####################################################################################################
    def synchronize_associations(supporter)
      ApplicationProcess.synchronize(Aethyr::Linux::Interface::ApplicationProcesses, supporter)
    end
    
  end  


######################################################################################################
protected

end

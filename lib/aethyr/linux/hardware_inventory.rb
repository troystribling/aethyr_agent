module Aethyr

  ############################################################################################################
  module Linux
  
    ##########################################################################################################
    class HardwareInventory

      ######################################################################################################
      #### class methods
      class << self

        ##########################################################################################################
        def build_database
          self.system
          self.memory
          self.cpu
          self.nic
        end
    
      ######################################################################################################
      end  

    ######################################################################################################
    protected

      ######################################################################################################
      #### class methods
      class << self

        ##########################################################################################################
        def memory
          Memory.destroy_all
          `cat /proc/meminfo`.split("\n").first
        end
  
        ##########################################################################################################
        def cpu
          Cpu.destroy_all
          `cat /proc/cpuinfo`
        end
  
        ##########################################################################################################
        def system
          System.destroy_all
          System.new(:name => `hostname`, :os => `uname -a`).save        
        end
  
        ##########################################################################################################
        def nic
          
        end

        ##########################################################################################################
        def find_system
          System.find(:first)
        end

      ######################################################################################################
      end  
      
    ##########################################################################################################
    end

  ##########################################################################################################
  end
  

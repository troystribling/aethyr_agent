############################################################################################################
module Aethyr

  ############################################################################################################
  module Linux
  
    ##########################################################################################################
    class Inventory
    
      ######################################################################################################
      #### class methods
      class << self

        ########################################################################################################
        def synchronize
          System.synchronize(Aethyr::Linux::System)
          sys = System.find(:first)
          Memory.synchronize(Aethyr::Linux::Memory, sys)
          Cpu.synchronize(Aethyr::Linux::Cpu, sys)
          Nic.synchronize(Aethyr::Linux::Nic, sys)
        end
  
      ######################################################################################################
      end  
      
    ##########################################################################################################
    end

  ##########################################################################################################
  end
  
##########################################################################################################
end
